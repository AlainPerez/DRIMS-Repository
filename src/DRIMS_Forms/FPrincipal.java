    private void jButton27ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton27ActionPerformed
        // TODO add your handling code here:
               if (jCheckBox4.isSelected()) {
                    ANTLRInputStream input = new ANTLRInputStream(jTextArea10.getText()+" --> "+jTextArea9.getText());
                    Data_Rules_CompilerLexer lexer = new Data_Rules_CompilerLexer(input);
                    CommonTokenStream tokens = new CommonTokenStream(lexer);
                    Data_Rules_CompilerParser parser = new Data_Rules_CompilerParser(tokens);
                    VerboseListener vl = new VerboseListener();
                    lexer.removeErrorListeners();//removeConsoleErrorListener
                    lexer.addErrorListener(vl);//addours
                    parser.removeErrorListeners();//removeConsoleErrorListener
                    parser.addErrorListener(vl);//addours
                    manage.setManagement(jComboBox13.getSelectedItem().toString());                    
                    parser.r_c.setMeta(manage);
                    parser.r_c.getCompiling_rule().setRelation(jTextField21.getText());
                    parser.r_c.setId_rule(jTextField19.getText());                    
                    parser.r_c.getCompiling_rule().setAntecedent(jTextArea10.getText());
                    parser.r_c.getCompiling_rule().setConsequent(jTextArea9.getText());              
                    Node node = my_nodeList.item(expl);
                    Element eElement = (Element) node;

                    if (eElement.getElementsByTagName("Association_Rule").getLength() > 0) 
                        parser.r_c.getCompiling_rule().setType("Association_Rule");
                       else if (eElement.getElementsByTagName("Approximate_Dependence").getLength() > 0)
                        parser.r_c.getCompiling_rule().setType("Approximate_Dependence");
                       else 
                        parser.r_c.getCompiling_rule().setType("Fuzzy_Association_Rule");

                    if ("Fuzzy_Association_Rule".equals(parser.r_c.getCompiling_rule().getType())){
                       NodeList nod_atrib_list = eElement.getElementsByTagName("Attribute");

                       for (int i = 0; i < nod_atrib_list.getLength(); i++){
                           Element elm_atrib = (Element) nod_atrib_list.item(i);
                           NodeList nod_atrib_ling_list = elm_atrib.getElementsByTagName("Linguistic_Label");

                           for (int j = 0; j < nod_atrib_ling_list.getLength(); j++){
                               Element elm_atrib_ling = (Element) nod_atrib_ling_list.item(j);
                               parser.r_c.getCompiling_rule().new_attribute_ling_label(elm_atrib.getElementsByTagName("Attribute_Name").item(0).getTextContent()
                                                                                    , elm_atrib_ling.getElementsByTagName("Label_Name").item(0).getTextContent()
                                                                                    , Double.valueOf(elm_atrib_ling.getElementsByTagName("A").item(0).getTextContent())
                                                                                    , Double.valueOf(elm_atrib_ling.getElementsByTagName("B").item(0).getTextContent())
                                                                                    , Double.valueOf(elm_atrib_ling.getElementsByTagName("C").item(0).getTextContent())
                                                                                    , Double.valueOf(elm_atrib_ling.getElementsByTagName("D").item(0).getTextContent()));
                           }

                       }
                    }

                    switch (parser.r_c.getCompiling_rule().getType()) {
                       case "Association_Rule":
                           parser.ar();
                           break;
                       case "Approximate_Dependence":
                           parser.ad();
                           break;
                       case "Fuzzy_Association_Rule":
                           parser.far();
                           break;
                   }
                    if (vl.par_lex_err.isEmpty() && parser.r_c.getError_messages().isEmpty()){
                        String script = parser.r_c.generate_script(jComboBox10.getSelectedItem().toString());
                        resources = parser.r_c.generate_resources(jComboBox10.getSelectedItem().toString());
                        jTextArea11.setText(script);
                        Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
                        Script_SQL.setLocation(dim.width/2-this.getSize().width/2, dim.height/2-this.getSize().height/2);
                        Script_SQL.setSize(488, 410);
                        Implement_Rule.setEnabled(false);                        
                        Script_SQL.setVisible(true);}
                    else {
                        String error = "";
                        int i = 0;
                        for (String err: vl.par_lex_err){               
                            error += "Error #"+String.valueOf(++i)+": "+err+"\n";
                        }            
                        for (String err: parser.r_c.getError_messages()){
                            error += "Error #"+String.valueOf(++i)+": "+err+"\n";
                        }
                        JOptionPane.showMessageDialog(null,"Fail rule validation: \n"+error,"PARSER ERROR",JOptionPane.ERROR_MESSAGE);
                    }
                }
            else JOptionPane.showMessageDialog(null,"The rule most be active for implementation.","ERROR",JOptionPane.ERROR_MESSAGE);
        
    }//GEN-LAST:event_jButton27ActionPerformed

