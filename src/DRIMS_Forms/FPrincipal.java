    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        // TODO add your handling code here:
        try {

            if (Double.parseDouble(jTextField5.getText())<=Double.parseDouble(jTextField7.getText()) && Double.parseDouble(jTextField7.getText())<=Double.parseDouble(jTextField9.getText()) && Double.parseDouble(jTextField9.getText())<=Double.parseDouble(jTextField6.getText()))
            {
                //rigth action heare
                if (jComboBox3.isVisible() && jComboBox3.getSelectedItem() != null){ 
                    String attribute = jComboBox3.getSelectedItem().toString();
                    attribute = attribute.substring(0, attribute.indexOf(" "));
                    if (new_rule.new_attribute_ling_label(attribute, jTextField8.getText(), Double.parseDouble(jTextField5.getText()), Double.parseDouble(jTextField7.getText()), Double.parseDouble(jTextField9.getText()), Double.parseDouble(jTextField6.getText())))
                    listModel.addElement(attribute+": "+jTextField8.getText());
                    else JOptionPane.showMessageDialog(null,"This attribute linguistic label alrready exists.","INFORMATION MESSAGE",JOptionPane.INFORMATION_MESSAGE);
                }
                else {
                    if (new_rule.new_attribute_ling_label("", jTextField8.getText(), Double.parseDouble(jTextField5.getText()), Double.parseDouble(jTextField7.getText()), Double.parseDouble(jTextField9.getText()), Double.parseDouble(jTextField6.getText())))
                    listModel.addElement(jTextField8.getText()+": ");
                    else JOptionPane.showMessageDialog(null,"This linguistic label alrready exists.","INFORMATION MESSAGE",JOptionPane.INFORMATION_MESSAGE);
                }

            }else {JOptionPane.showMessageDialog(null,"Verify that A <= B <= C <= D.","ERROR",JOptionPane.ERROR_MESSAGE);}

        } catch (java.lang.NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"Verify that A, B, C and D are integer or float data types.","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        if (jList1.getModel().getSize()>1) {jButton9.setEnabled(true);}
    }//GEN-LAST:event_jButton12ActionPerformed

