    private void ShowAllRules_Expl() {
       
      Node node = my_nodeList.item(expl);
      Element eElement = (Element) node;
      jTextField10.setText(eElement.getAttribute("rule_id"));
      jTextField11.setText(eElement.getElementsByTagName("Date").item(0).getTextContent());
      jCheckBox1.setSelected("true".equals(eElement.getElementsByTagName("Active").item(0).getTextContent()));      
      jTextField12.setText(eElement.getElementsByTagName("Relation").item(0).getTextContent());
      jTextArea3.setText(eElement.getElementsByTagName("Antecedent").item(0).getTextContent());
      jTextArea6.setText(eElement.getElementsByTagName("Consequent").item(0).getTextContent());
      if (eElement.getElementsByTagName("Association_Rule").getLength() > 0) 
       jTextField13.setText("Association Rule");
      else if (eElement.getElementsByTagName("Approximate_Dependence").getLength() > 0)
       jTextField13.setText("Approximate Dependence");
      else 
       jTextField13.setText("Fuzzy Association Rule");   
    }
    
