    private void jButton46ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton46ActionPerformed
        // TODO add your handling code here:
         String Path = null;
         if (directory[0]== null){directory[0] = ".";} 
         JFileChooser fc = new JFileChooser(directory[0]);
         fc.setDialogTitle("Specify a SQL script to save");
         File file = new File("untitled.sql");  //SEEEEEEE
         fc.setSelectedFile(file);
         fc.addChoosableFileFilter(new FileFilter() {
         @Override
         public String getDescription() {
         return "SQL Script (*.sql)";
         }
         @Override
         public boolean accept(File f) {
         if (f.isDirectory()) {
            return true;
         } else {
            return f.getName().toLowerCase().endsWith(".sql");
         }
         }
         });
         if (fc.showSaveDialog(null) == JFileChooser.APPROVE_OPTION) {
         Path = fc.getSelectedFile().getAbsolutePath();
         directory[0] = fc.getCurrentDirectory().toString();
         directory[1] = fc.getSelectedFile().getName();
         } else JOptionPane.showMessageDialog(null, "No file selected.");
         try {
             try (BufferedWriter fileOut = new BufferedWriter(new FileWriter(Path))) {
                 jTextArea11.write(fileOut);
             }
             JOptionPane.showMessageDialog(null,"Script successfully saved!","SAVE TO FILE",JOptionPane.INFORMATION_MESSAGE);
         }  catch (IOException e) {
            JOptionPane.showMessageDialog(null,"Error: "+e.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
         }
    }//GEN-LAST:event_jButton46ActionPerformed

