    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        // TODO add your handling code here:

        ANTLRInputStream input = new ANTLRInputStream(jTextArea1.getText()+" --> "+jTextArea2.getText());
        Data_Rules_CompilerLexer lexer = new Data_Rules_CompilerLexer(input);
        CommonTokenStream tokens = new CommonTokenStream(lexer);
        Data_Rules_CompilerParser parser = new Data_Rules_CompilerParser(tokens);
        VerboseListener vl = new VerboseListener();
        lexer.removeErrorListeners();//removeConsoleErrorListener
        lexer.addErrorListener(vl);//addours
        parser.removeErrorListeners();//removeConsoleErrorListener
        parser.addErrorListener(vl);//addours
        parser.r_c.setCompiling_rule(new_rule);        
        parser.r_c.setMeta(manage); 
        switch (parser.r_c.getCompiling_rule().getType()) {
        case "Association_Rule":
            parser.ar();
            break;
        case "Approximate_Dependence":
            parser.ad();
            break;
        case "Fuzzy_Association_Rule":
            parser.far();
            break;
        }
        
        if (vl.par_lex_err.isEmpty() && parser.r_c.getError_messages().isEmpty()){
           JOptionPane.showMessageDialog(null,"Correct rule validation!","Validation",JOptionPane.INFORMATION_MESSAGE);
           jButton13.setEnabled(true); 
        }
        else {
            String error = "";
            int i = 0;
            for (String err: vl.par_lex_err){               
                error += "Error #"+String.valueOf(++i)+": "+err+"\n";
            }            
            for (String err: parser.r_c.getError_messages()){
                error += "Error #"+String.valueOf(++i)+": "+err+"\n";
            }
            JOptionPane.showMessageDialog(null,"Fail rule validation: \n"+error,"PARSER ERROR",JOptionPane.ERROR_MESSAGE);
        }        
    }//GEN-LAST:event_jButton16ActionPerformed

